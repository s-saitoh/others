class JSample1_1{
    public static void main(String args[]){

        for (int i = 0 ; i < 5 ; i++){
            if (i % 2 == 0){
                System.out.println(i + "は偶数です");
            }
            if (i % 2 != 0){
                System.out.println(i + "は奇数です");
            }

        }
    }
}